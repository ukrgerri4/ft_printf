#include "libft.h"

char    *ft_tolower_str(char *s)
{
    int i;

    i = 0;
    if (s)
    {
        while (s[i])
        {
	        if (s[i] >= 'A' && s[i] <= 'Z')
		        s[i] = s[i] + 32;
            i++;
        }
        return (s);
    }
    return (NULL);
}
